    <p class="lead">
    &nbsp;&nbsp;&nbsp;This was one of the longest segment of the course (in between aid stations) which tested my patience &amp; my hunger pangs coupled with my exhaustion made things worse. I was down to my last drop of water when I arrived at WS9.
    </p>

    <h2>Hunger</h2>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;In my backpack, I carried 1 energy gel, 2 muesli bars and a dozen Iranian dates. But during the 2nd 50km, I could only force myself to swallow the dates. I had no appetite for the gels or muesli bars. Instead, I was craving for salty foods. After WS9, if you had given me a cup of seawater, I would have downed it in one shot.
    </p>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;There is an easy explanation for my aversion to sweet things: I am a salty sweater &amp; I sweat a lot. I believe that I was already low on sodium when I arrived at WS6 (50 km). I felt dizzy and decided to have my blood pressure checked out by the medical team after ingesting some porridge. But I forgot to do that.
    </p>

    <p style="margin 0 auto; text-align: center;">
    <button class="btn btn-large btn-primary btn-danger" type="button">
    <i class="icon-warning-sign"></i>
    Low on sodium! Please replenish ASAP!</button>
    </p>

    <p class="lead">&nbsp;&nbsp;&nbsp;There is a precedent to my low sodium situation. At the Boston marathon in April 2012, I had to be wheelchaired into the medical tent &amp; almost passed out due to hypotension (low blood pressure). They measured it at 70/60. During the race, I rejected the sweet gatorade drink and was severely dehydrated as a result.</p>


    <p class="lead">
    &nbsp;&nbsp;&nbsp;On the descent to WS11, my stomach felt like it was being tied in knots. Luckily for me, the official at WS11 gave me a box of rice with scrambled eggs. Real food never tasted so good. In future ultras; I am going to pack salty Malaysian dishes in small sachets &amp; carry them instead of gels or energy bars. 
    </p>

    <ul>
      <li>2x sachets of fried rice with MSG.</li>
      <li>1x sachet of salty <a href="http://en.wikipedia.org/wiki/Char_kway_teow" title="wikipedia" target="_blank">Char Kuew Teow</a>.</li>
    </ul>

    <h2>Resilience</h2>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;I've learnt that one cannot let negative thoughts crowd your mind when you're on a journey like a 100 km ultramarathon. Actually, you only need a little nudge to stay optimistic.
    </p>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;<strong>Exhibit A</strong>: Runner 415 was suffering from cramps on the climb up from WS4. When I passed him, he was telling me that he cannot finish the 100 km in his current state. I turned to him and said, "<em>just take your time and bring yourself to the 50 km checkpoint. Rest and have something to eat then only decide on whether you want to quit there</em>". Remarkably he managed to finish the 100 km in 17th place. <i class="icon-leaf"></i>
    </p>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;As for me, I never doubted my will power to complete the race. But it was nice to get an SMS from a 50 km runner acquaintance, Faisal (whose mobile number is saved as <em>Dvilla JB couple</em> in my phone), asking me how I was during my ascent to WS8. That SMS exchange was a mini life-saver. <i class="icon-ok"></i>
    </p>

