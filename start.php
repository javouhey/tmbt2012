    <h1>Postmortem</h1>


    <blockquote>
    <p style="font-style: italic;">Aku anak kampung,</p>
    <p style="font-style: italic;">tiada pelajaran,</p>
    <p style="font-style: italic;">kuli kuli sahaja.</p>
    <small>Jimmy Palikat&nbsp;<cite title="youtube video"><a href="http://www.youtube.com/watch?v=Hratiifi7ps" target="_blank" title="Anak Kampung music video">Anak Kampung</a></cite></small></blockquote>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;<strong>Sep 17th 2012</strong>: I'm seated inside a Toyota Unser leaving Mt. Kinabalu Park and headed for <abbr title="Kota Kinabalu">KK</abbr>. Jimmy Palikat's <em>Anak Kampung</em> song was playing on the car stereo. With time on my hands, I performed a mini postmortem of my race. 
    </p>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;Firstly, I believed I had treated tmbt 100km purely as a means to an end; the end being the 2 UTMB points. I never imagined that the course promised an elevation gain of 4350m. In my opinion, running tmbt was like running another CCC two weeks later. It deserves at least 2.5 UTMB points or more. Thus the <strong>b</strong> in <strong>tmbt</strong> should be rebranded <em>brutal and beautiful</em>.
    </p>

