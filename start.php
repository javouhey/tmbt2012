    <h1>Motivation</h1>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;The journey that I referred to above is a 100 km single stage footrace around the hillsides surrounding Kundasang &amp; Ranau; all under the shadow of Mount Kinabalu. One of the reasons that I signed up for the race was because I wanted the two <abbr title="Ultra-trail du Mont-Blanc">UTMB</abbr> points on offer.<i class="icon-gift"></i></p> 

    <p class="lead">
    &nbsp;&nbsp;&nbsp;Two weeks earlier (31st August 2012), I participated in the 100 km <abbr title="Courmayeur Champex Chamonix">CCC</abbr> but just as it was in the 2011th edition, the course had to be truncated due to <a href="http://goo.gl/Nf9ur" target="_blank" title="Youtube video at Grand col ferret in atrocious weather at CCC 2012">bad weather</a>. Thus, <a href="http://www.sabahadventurechallenge.com/ultra/" target="_blank">tmbt</a> offered me another chance to finally nail the full 100 km distance.
    </p>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;The race briefing helped me to come up with these 4 coping strategies:
    </p>

    <ol>
      <li>Don't injure yourself</li>
      <li>Keep your shoes dry</li>
      <li>Stay positive even if it hurts</li>
      <li>Don't get lost</li>
    </ol>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;In this race report, I will describe the race in short vignettes organized around 6 themes. <i class="icon-th"></i>
    </p>

    <h2>Nature</h2>

    <blockquote>
    <p style="font-style: italic;">After you have exhausted what there is in business, politics, or conviviality, and so on - have found that none of these finally satisfying, or permanently new - what remains ?  Nature remains.</p>
    <small>Walt Whitman</small> 
    </blockquote>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;Runners enjoyed mostly good weather on race day. In perfect conditions, I enjoyed negotiating the vegetable farms in the early kilometres. I got sprayed in the face by strategically placed water sprinklers, jumped over little streams, clambered over rocks, &amp; breathed in fresh mountain air amongst others.</p>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;The terrain was mixed: a little bit of tarred roads, unpaved roads, jungle trails, semi-completed roads, roads fit only for four-wheel drive vehicles, soft-soiled single-track roads, etc. I had a lot of problems with the gravel roads; my feet didn't like them. After 50 kms, even walking was painful.<i class="icon-thumbs-down"></i> 
    </p>

