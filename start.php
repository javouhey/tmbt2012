    &nbsp;&nbsp;&nbsp;For some runners, it was nerve-wracking to have to run over roads with risks of landslides. I was a bit nonchalant about it. In the papers from that weekend, I read about families living in Tamparuli (Sabah) whose houses were threatened by landslides. They worry for their safety every day. We runners just need to be worried about a kilometre-or-so stretch of road. 
    </p>

