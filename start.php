    </p>


    <p class="lead">&nbsp;&nbsp;&nbsp;During the night descent from WS9 to WS10, I developed a strategy to catch my breath. I placed my water bottle on the ground in front of me (<strong>note to self</strong>: <em>use walking poles for my next ultra</em>). With one hand resting on the bottle, I counted from 1 to 10 while I took deep breaths. On one occasion, my headlamp captured an ant trail as depicted below.</p>

