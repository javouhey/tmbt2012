    <h2>Solitude</h2>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;For the first 50 kms, I always had some runners within sight. As day turned into night <i class="icon-adjust"></i>, I found myself alone on the race course. 
    </p>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;It was disconcerting for me as I generally use the presence of other runners to confirm that I'm not lost. If my memory serves me right, I was virtually alone during the trek from WS9 (~75 km) to WS11 (~87 km). <i class="icon-map-marker"></i>
    </p>
    <blockquote><p style="font-style: italic;">I think I'd like to tell them only this, to know how to find themselves more often alone - to enjoy being alone with themselves more frequently. I think the tragedy of today's youth is that they try to unite on the basis some noisy events, a couple even violent, and this desire to unite in order not not to be alone is an unpleasant condition, from my point of view. I think a person needs to learn from childhood to find himself alone.</p>
    <small>Andrei Tarkovsky <cite title="youtube video"><a href="http://www.youtube.com/watch?v=_Vvdtaaprzw" target="_blank" title="A message to young people video">a message to young people</a></cite></small> 
    </blockquote>
    <p class="lead">
    &nbsp;&nbsp;&nbsp;After WS11, I followed a group of runners to the finish line.</p>


    <h2>Hills</h2>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;This was my favorite part of the race because runners were totally shielded from car or human traffic. It's just you against the hill. <i class="icon-arrow-up"></i></p>

    <h4>After WS4 (~37 km)</h4>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;This is a jungle trail and some parts had knee-high grass. Extra care was required to avoid getting scraped by the <em>daun gatal</em> (nettles) lining the route (<strong>note to self</strong>: wear <em>chaussette longue</em> next time). I didn't encounter any snakes.
    </p>

    <h4>55K to WS7(60 km)</h4>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;A runner was so impressed with the grade of the climb that he took pictures of it. I'm not sure if it was a 45% grade slope but it was close in some parts. This was the first of many hills that made me question the sanity of their architects.
    </p>

    <h4>WS8(65 km) to WS9 (~75 km)</h4>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;After signing off my name at the <em>Kampung Karanaan</em> checkpoint, a woman asked me from her porch - "<em>Berani tak masuk hutan gelap</em> ?" (forgive me for my broken Malay but she asked if I'm brave enough to enter the forest in the dark). I answered yes and started the climb.
    </p>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;I huffed and puffed up the hill and suddenly heard explosions behind me. They were releasing fireworks to commemorate <a href="http://en.wikipedia.org/wiki/Malaysia_Day" title="wikipedia" target="_blank">Malaysia Day</a>.</p>

    <p class="lead">
    &nbsp;&nbsp;&nbsp;Every now and then, I would stop climbing &amp; gazed up at the cloudless night sky. The stars were out in force that night. <i class="icon-asterisk"></i>
    </p>

